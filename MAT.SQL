 
SELECT 
VOUCHERNUM,ITEMNUM,DESCRIPTION,TOSTORELOC, PONUM,
   ACTUALDATE, QUANTITY,CURRENCYUNITCOST,CURRENCYLINECOST,CURRENCYCODE,

EXCHANGERATE,(CURRENCYUNITCOST * EXCHANGERATE) as LYD_UNITCOST,
(CURRENCYLINECOST * EXCHANGERATE) as LYD_LINECOST 
,ISSUE,GLDEBITACCT, GLCREDITACCT 
     
   
FROM MAXIMO.V_MATRECTRANS where  ACTUALDATE between '01-APRIL-2021' and '01-May-2021' and ISSUE='N' 
and VOUCHERNUM is not null order by ACTUALDATE desc;